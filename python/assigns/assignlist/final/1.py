import math

#returns the square root of a number as a float
print("Square root: ",math.sqrt(25))
#raises argument 1 to the power of argument 2
print("To the power of 2: ",math.pow(10,2))
#calculates e to the power 4
print("Exponential: ",math.exp(4))
print("Natural Logarithm: ",math.log(16,2))
print("Common Logarithm: ",math.log10(100))
#trigonometric functions, arguments in radians
print("Sine: ",math.sin(30))
print("Cosine: ",math.cos(30))
print("Tangent: ",math.tan(30))
#inverse trigonometric functions, arguments in radians
print("Arc Sine: ",math.asin(0.5))
print("Arc Coine: ",math.acos(0.5))
print("Arc Tangent: ",math.atan(0.5))
print("Ceil: ",math.ceil(3.6))
print("Floor: ",math.floor(3.6))
print("Absolute: ",math.fabs(-4.2))
print("Hypotenuse: ",math.hypot(3,4))
print("GCD: ",math.gcd(4,8))
# the round function rounds floating point numbers to specified number of digits
print("Area of a circle(r=2): ",round(math.pi*2*2,4))
print("Perimeter of a circle(r=2): ",round(2*math.pi*2,2))
