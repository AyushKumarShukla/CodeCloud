"""
Total money in piggy bank given coins of 10,5,2,1
"""
print("****WELCOME TO PIGGY BANK****")
ten=int(input("ENTER THE NUMBER OF 10 RUPEE COINS: "))
five=int(input("ENTER THE NUMBER OF 5 RUPEE COINS: "))
two=int(input("ENTER THE NUMBER OF 2 RUPEE COINS: "))
one=int(input("ENTER THE NUMBER OF 1 RUPEE COINS: "))
total=ten*10+five*5+two*2+one*1
print("TOTAL AMOUNT: "+str(total))
